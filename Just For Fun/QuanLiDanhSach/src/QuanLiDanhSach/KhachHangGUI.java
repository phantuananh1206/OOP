    private void btthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btthemActionPerformed
        // TODO add your handling code here:
          String ma= this.tfma.getText();
            String ht= this.tften.getText();
            Date nkh = new Date();
            DateFormat df= new SimpleDateFormat("yyyy-m-d");
        try {
            nkh= df.parse(this.tfngay.getText());
        } catch (ParseException ex) {}
        int sl= Integer.parseInt(this.tfsoluong.getText());
            double dongia= Double.parseDouble(this.tfdongia.getText());
         KhachHang v;
         if(this.rbKHVietNam.isSelected()){
             String dtkh= this.tfdoituongkh.getText();
             double dm = Double.parseDouble(this.tfdinhmuc.getText());
             v= new KhachHangVietNam(dtkh, dm, dtkh, ht, nkh, sl, dongia);
         }
         else {
             String qt= this.tfquoctich.getText();
             v=new KhachHangNuocNgoai(qt, ma, ht, nkh, sl, dongia);
         }
         ds.them(v);
         this.xuatTable(ds.getM());
    }//GEN-LAST:event_btthemActionPerformed

