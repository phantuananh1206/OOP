    private void xuatTable(ArrayList<KhachHang> arr){
        for(int i=dm.getRowCount()-1;i>=0;i--)
            dm.removeRow(i);
        for(KhachHang v:arr)
            if(v instanceof KhachHangVietNam)
                dm.addRow(new Object[]{v.getMaKhachHang(),v.getHoTen(),v.getNgayHoaDon(),v.getSoLuong(),v.getDonGia(),((KhachHangVietNam)v).getDoiTuongKH(),((KhachHangVietNam)v).getDinhMuc()});
            else
                dm.addRow(new Object[]{v.getMaKhachHang(),v.getHoTen(),v.getNgayHoaDon(),v.getSoLuong(),v.getDonGia(),"","",((KhachHangNuocNgoai)v).getQuocTich()});
    }

