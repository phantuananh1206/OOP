    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:      
        String maKH = JOptionPane.showInputDialog("Nhap ma Khach Hang muon cap nhat");
        KHACHHANG khachHang = danhSach.timKiemKH(maKH);        
        index = danhSach.HienThi().indexOf(khachHang);
        dtmCapNhat = (DefaultTableModel)tblUpdate.getModel();
        tblUpdate.setModel(dtmCapNhat); 
         try {
              ClearOldDataTable(dtmCapNhat);
            dtmCapNhat.addRow(new Object[]{
                khachHang.getMaKH(),
                khachHang.getHoTen(),
                khachHang.getNgayRaHoaDon(),
                khachHang.getSoLuong(),
                khachHang.getDonGia(),
                ((KHACHHANGVIETNAM) khachHang).getDoiTuongKH(),
                null,
                ((KHACHHANGVIETNAM) khachHang).getDinhMuc()});
        } catch(ClassCastException e) {
             ClearOldDataTable(dtmCapNhat);
            dtmCapNhat.addRow(new Object[]{
                khachHang.getMaKH(),
                khachHang.getHoTen(),
                khachHang.getNgayRaHoaDon(),
                khachHang.getSoLuong(),
                khachHang.getDonGia(),
                null,
                ((KHACHHANGNUOCNGOAI) khachHang).getQuocTich(),
                null});
        }
        
    }//GEN-LAST:event_btnCapNhatActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        KHACHHANG kh = danhSach.HienThi().get(index);
        try {
            kh.setMaKH(dtmCapNhat.getValueAt(0, 0).toString());
            kh.setHoTen(dtmCapNhat.getValueAt(0, 1).toString());
            kh.setNgayRaHoaDon(chuyenChuoiThanhNgay(dtmCapNhat.getValueAt(0, 2).toString()));
            kh.setSoLuong(Double.parseDouble(dtmCapNhat.getValueAt(0, 3).toString()));
            kh.setDonGia(Double.parseDouble(dtmCapNhat.getValueAt(0, 4).toString()));
            ((KHACHHANGVIETNAM)kh).setDoiTuongKH(dtmCapNhat.getValueAt(0, 5).toString());
            ((KHACHHANGVIETNAM)kh).setDinhMuc(Double.parseDouble(dtmCapNhat.getValueAt(0, 7).toString()));
            JOptionPane.showMessageDialog(null, "Cap nhat thanh cong");
        } catch (ClassCastException e) {
            kh.setMaKH(dtmCapNhat.getValueAt(0, 0).toString());
            kh.setHoTen(dtmCapNhat.getValueAt(0, 1).toString());
            kh.setNgayRaHoaDon(chuyenChuoiThanhNgay(dtmCapNhat.getValueAt(0, 2).toString()));
            kh.setSoLuong(Double.parseDouble(dtmCapNhat.getValueAt(0, 3).toString()));
            kh.setDonGia(Double.parseDouble(dtmCapNhat.getValueAt(0, 4).toString()));
            ((KHACHHANGNUOCNGOAI)kh).setQuocTich(dtmCapNhat.getValueAt(0, 6).toString());
            JOptionPane.showMessageDialog(null, "Cap nhat thanh cong");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

