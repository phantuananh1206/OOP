    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String doiTuongKH = cbDoiTuongKH.getSelectedItem().toString();
        if (cbLoaiKhachHang.getSelectedIndex() == 0) {           
            danhSach.themMoi(new KHACHHANGVIETNAM(
                    doiTuongKH,
                    Double.parseDouble(txtDinhMuc.getText().trim()),
                    txtMaKhachHang.getText().trim(),
                    txtHoTen.getText().trim(),
                    chuyenChuoiThanhNgay(txtNgayHoaDon.getText().trim()),
                    Double.parseDouble(txtSoLuong.getText().trim()), 
                    Double.parseDouble(txtDonGia.getText().trim())));
            AddDataToTable();
            ClearAllTextField();
        } else {
            danhSach.themMoi(new KHACHHANGNUOCNGOAI(
                    txtQuocTich.getText().trim(), 
                    txtMaKhachHang.getText().trim(), 
                    txtHoTen.getText().trim(), 
                    chuyenChuoiThanhNgay(txtNgayHoaDon.getText().trim()), 
                    Double.parseDouble(txtSoLuong.getText().trim()), 
                    Double.parseDouble(txtDonGia.getText().trim())));
            AddDataToTable();
            ClearAllTextField();
        }
    }//GEN-LAST:event_btnThemActionPerformed

