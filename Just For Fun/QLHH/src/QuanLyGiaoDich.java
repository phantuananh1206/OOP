    private void thietLapKhoa(int loaiHangHoa) {
        boolean OK = true;
        if (loaiHangHoa == 0){
            OK = false;
        }
        
        jcbNoiSanXuat.setEnabled(!OK);
        txtNhaSanXuat.setEnabled(OK);
    }
    private String chuyenDateThanhString(Date date){
        return new SimpleDateFormat("dd/MM/yyyy").format(date);
    }
    
    private Date chuyenStringThanhDate(String date){
        try {
            return new SimpleDateFormat("dd/MM/yyyy").parse(date);
        } catch (Exception e) {
        e.printStackTrace();
        }
        return null;
    }

    private void napDuLieuVaoBang(DefaultTableModel dtm) {
        xoaDuLieuCu(dtm);
        int stt = 0;
        for (HangHoa hh : danhSachs.hangHoas) {
            themMotDong(++stt, hh, dtm);
        }
    }

    private void hienThiThongBao(String thongBao) {
        JOptionPane.showMessageDialog(null, thongBao);
    }

    private void clearAllField() {
        txtDonGia.setText("");
        txtMaHang.setText("");
        txtNgayNhap.setText("");
        txtNhaSanXuat.setText("");
        txtSoLuong.setText("");
    }

    private void xoaDuLieuCu(DefaultTableModel dtm) {
        while(dtm.getRowCount() > 0){
            dtm.removeRow(0);
        }
    }

    private void themMotDong(int stt, HangHoa hh, DefaultTableModel dtm) {
        if (hh instanceof HangDienMay){
            dtm.addRow(new Object[]{
                "" + stt, hh.getMaHang() , chuyenDateThanhString(hh.getNgayNhap()), hh.getDonGia(), hh.getSoLuong(), ((HangDienMay) hh).getNoiSanXuat(), null , ((HangDienMay) hh).tinhThanhTien(), ((HangDienMay) hh).tinhTienLuuKho()
            });
        }else{
            dtm.addRow(new Object[]{
                "" + stt, hh.getMaHang() , chuyenDateThanhString(hh.getNgayNhap()), hh.getDonGia(), hh.getSoLuong(), null, ((HangThuCong) hh).getNhaSanXuat() , ((HangThuCong) hh).tinhThanhTien(), ((HangThuCong) hh).tinhTienLuuKho()
            });
        }
    }
    private void themMotDongTTT(int stt, String hh){
        if (hh.equalsIgnoreCase("Hàng điện máy")){
            dtmTongThanhTien.addRow(new Object[]{
                "" + stt, "Hàng điện máy", danhSachs.tingTongThanhTien(hh)
            });
        }
         if (hh.equalsIgnoreCase("Hàng thủ công")){
            dtmTongThanhTien.addRow(new Object[]{
                "" + stt, "Hàng thủ công", danhSachs.tingTongThanhTien(hh)
            });
        }
    }
