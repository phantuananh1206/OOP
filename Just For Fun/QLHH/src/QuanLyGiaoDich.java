    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (jcbLoaiHangHoa.getSelectedIndex() == 0){
                    danhSachs.them(new HangDienMay((String) jcbNoiSanXuat.getSelectedItem(), 
                    txtMaHang.getText().trim(), 
                    chuyenStringThanhDate(txtNgayNhap.getText().trim()), 
                    Double.parseDouble(txtDonGia.getText().trim()), 
                    Double.parseDouble(txtSoLuong.getText().trim())));
                    napDuLieuVaoBang(dtmHangHoas);
                    hienThiThongBao("Thêm thành công");
                    clearAllField();
        }else{
                    danhSachs.them(new HangThuCong(txtNhaSanXuat.getText().trim(), 
                    txtMaHang.getText().trim(), 
                    chuyenStringThanhDate(txtNgayNhap.getText().trim()), 
                    Double.parseDouble(txtDonGia.getText().trim()), 
                    Double.parseDouble(txtSoLuong.getText().trim())));
                    napDuLieuVaoBang(dtmHangHoas);
                    hienThiThongBao("Thêm thành công");
                    clearAllField();
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        String ma = JOptionPane.showInputDialog("Nhập mã cần xóa : ");
        if (danhSachs.xoa(ma)){
            napDuLieuVaoBang(dtmHangHoas);
            hienThiThongBao("Xóa thành công");
        }else{
            hienThiThongBao("Không tìm thấy mã cần xóa");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
         String ma = JOptionPane.showInputDialog("Nhập mã cần tìm : ");
         if (danhSachs.timKiem(ma)!= null){
             xoaDuLieuCu(dtmHangHoas);
             themMotDong(1, danhSachs.timKiem(ma), dtmHangHoas);
             hienThiThongBao("Tìm thành công");
        }else{
            hienThiThongBao("Không tìm thấy ");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

