    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        for (KhachHang khachHang : khachHangs.getDanhSach()) {
            if (khachHang.getMaKhString().equalsIgnoreCase(tfMaKh.getText().trim())){
                if (loaiKhachHang){
                    khachHang = (new KhachHangVietNam(tfDoiTuongKH.getText().trim(), Double.parseDouble(tfDinhMuc.getText().trim()), tfMaKh.getText().trim(), tfTenKh.getText().trim(), stringToDate(tfNgayRaHD.getText().trim()), Double.parseDouble(tfSoLUong.getText().trim()), Double.parseDouble(tfDonGia.getText().trim())));
                } else {
                    khachHang = (new KhachHangNuocNgoai(tfQuocTich.getText().trim(), tfMaKh.getText().trim(), tfTenKh.getText().trim(), stringToDate(tfNgayRaHD.getText().trim()), Double.parseDouble(tfSoLUong.getText().trim()), Double.parseDouble(tfDonGia.getText().trim())));
                }
                khachHangs.capNhat(khachHang);
            }
        }
        reloadJTable();
    }//GEN-LAST:event_btnCapNhatActionPerformed

