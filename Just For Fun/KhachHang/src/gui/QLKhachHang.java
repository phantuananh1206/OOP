    private void myInits() {
        setTitle("Quan ly khach hang");
        dtm = (DefaultTableModel) JTable.getModel();
        JTable.setModel(dtm);
        khachHangs = new DSKhachHang();
        setEnablejTextField(loaiKhachHang);
    }

    private Date stringToDate(String trim) {
        try {
            return new SimpleDateFormat("dd/MM/yyyy").parse(trim);
        } catch (ParseException ex) {
            Logger.getLogger(QLKhachHang.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }

    private boolean  themKhachHang() {
        if (loaiKhachHang){
            return khachHangs.themKhachHang(new KhachHangVietNam(tfDoiTuongKH.getText().trim(), Double.parseDouble(tfDinhMuc.getText().trim()), tfMaKh.getText().trim(), tfTenKh.getText().trim(), stringToDate(tfNgayRaHD.getText().trim()), Double.parseDouble(tfSoLUong.getText().trim()), Double.parseDouble(tfDonGia.getText().trim())));
        } else {
            return khachHangs.themKhachHang(new KhachHangNuocNgoai(tfQuocTich.getText().trim(), tfMaKh.getText().trim(), tfTenKh.getText().trim(), stringToDate(tfNgayRaHD.getText().trim()), Double.parseDouble(tfSoLUong.getText().trim()), Double.parseDouble(tfDonGia.getText().trim())));
        }
    }

    private void reloadJTable() {
        removeAllElementJTable();
        removeAllTextField();
        for (KhachHang khachHang : khachHangs.getDanhSach()) {
            if (khachHang instanceof KhachHangVietNam){
                dtm.addRow(new Object[] {
                    khachHang.getMaKhString(),
                    khachHang.getHoTen(),
                    new SimpleDateFormat("dd/MM/yyyy").format(khachHang.getNgayRaHoaDon()),
                    khachHang.getSoLuong(),
                    khachHang.getDonGia(),
                    ((KhachHangVietNam) khachHang).getDoiTuongKhachHang(),
                    ((KhachHangVietNam) khachHang).getDinhMuc(),
                    "Viet Nam"
                });
            } else {
                dtm.addRow(new Object[] {
                    khachHang.getMaKhString(),
                    khachHang.getHoTen(),
                    new SimpleDateFormat("dd/MM/yyyy").format(khachHang.getNgayRaHoaDon()),
                    khachHang.getSoLuong(),
                    khachHang.getDonGia(),
                    null,
                    null,
                    ((KhachHangNuocNgoai) khachHang).getQuocTich()
                });
            }
        }
    }

    private void removeAllElementJTable() {
        while(dtm.getRowCount() > 0)
            dtm.removeRow(0);
    }

    private void removeAllTextField() {
        tfMaKh.setText("");
        tfTenKh.setText("");
        tfSoLUong.setText("");
        tfNgayRaHD.setText("");
        tfDoiTuongKH.setText("");
        tfDinhMuc.setText("");
        tfDonGia.setText("");
        tfQuocTich.setText("");
    }

    private void setEnablejTextField(boolean bool) {
        tfQuocTich.setEnabled(!bool);
        tfDinhMuc.setEnabled(bool);
        tfDoiTuongKH.setEnabled(bool);
    }
