    private Date StringThanhDate(String indate){
        try {
            return new SimpleDateFormat("dd/MM/yyyy").parse(indate);
        } catch (ParseException e) {
            return null;
        }
    }

    private void napDuLieu() {
        xoadulieucu(dtmkhachhangs);
        int stt=0;
        for (KHACHHANG kh : danhSach.khachhangs) {
            themMotDong(++stt,kh);
        }
    }

    
    private void themMotDong(int stt,KHACHHANG kh) {
        if(kh instanceof KHACHHANGVIETNAM){
            dtmkhachhangs.addRow(new Object[]{stt,kh.getMaKH(),kh.getHoTen(),kh.getSoLuong(),DatesangString(kh.getNgayHD()),kh.getDongia(),((KHACHHANGVIETNAM) kh).getDoiTuongKH(),((KHACHHANGVIETNAM) kh).getDinhMuc(),null,((KHACHHANGVIETNAM) kh).tinhThanhTien()});
        }
        if(kh instanceof KHACHHANGNUOCNGOAI){
            dtmkhachhangs.addRow(new Object[]{stt,kh.getMaKH(),kh.getHoTen(),kh.getSoLuong(),DatesangString(kh.getNgayHD()),kh.getDongia(),null,null,((KHACHHANGNUOCNGOAI) kh).getQuocTich(),((KHACHHANGNUOCNGOAI) kh).tinhThanhTien()});
        }
    }
    
    
