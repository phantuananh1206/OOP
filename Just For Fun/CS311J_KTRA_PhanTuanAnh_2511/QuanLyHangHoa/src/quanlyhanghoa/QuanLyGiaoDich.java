    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (jcbLoaiHangHoa.getSelectedIndex() == 0){
            danhSachs.themMoi(new HangDienMay((String) jcbNoiSanXuat.getSelectedItem(),
                    txtMaHang.getText().trim(),
                    chuyenStringThanhDate(txtNgayNhap.getText().trim()),
                    Double.parseDouble(txtDonGia.getText().trim()), 
                    Double.parseDouble(txtSoLuong.getText().trim())));
                    napDuLieuVaoBang(dtmHangHoas);
                    hienThiThongBao("Thêm thành công");
                    clearAllField();
        }else{
            danhSachs.themMoi(new HangThuCong(txtNhaSanXuat.getText().trim(),
                    txtMaHang.getText().trim(),
                    chuyenStringThanhDate(txtNgayNhap.getText().trim()),
                    Double.parseDouble(txtDonGia.getText().trim()), 
                    Double.parseDouble(txtSoLuong.getText().trim())));
                    napDuLieuVaoBang(dtmHangHoas);
                    hienThiThongBao("Thêm thành công");
                    clearAllField();
        }
    }//GEN-LAST:event_btnThemActionPerformed

