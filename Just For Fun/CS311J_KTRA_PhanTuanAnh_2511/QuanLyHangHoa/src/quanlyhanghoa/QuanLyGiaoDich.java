    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String ma = JOptionPane.showInputDialog("Nhập mã cần xóa : ");
        if (danhSachs.xoa(ma)){
            napDuLieuVaoBang(dtmHangHoas);
            hienThiThongBao("Xóa thành công");
        }else{
            hienThiThongBao("Xóa không thành công");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String ma = JOptionPane.showInputDialog("Nhập mã cần tìm : ");
        if (danhSachs.timKiem(ma)!= null){
            xoaDuLieuCu(dtmHangHoas);
            themMotDong(1, danhSachs.timKiem(ma), dtmHangHoas);
            hienThiThongBao("Tìm thành công");
        }else{
            hienThiThongBao("Không tìm thấy");
        }
        
    }//GEN-LAST:event_btnTimKiemActionPerformed

