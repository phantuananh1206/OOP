    private void myInits() {
        setTitle("Quan Ly giang vien");

        setLocationRelativeTo(null);

        dtmGiangViens = (DefaultTableModel) jtbDanhSach.getModel();
        jtbDanhSach.setModel(dtmGiangViens);

        danhSachs = new DSGV();

        thietLapKhoa(0);

    }

    private void thietLapKhoa(int loaiGiangVien) {
        boolean OK = true;
        if (loaiGiangVien == 0) {
            OK = false;
        }
        txtNgayVao.setEnabled(!OK);
        txtLuongCoBan.setEnabled(!OK);
        txtMaHopDong.setEnabled(OK);

    }

    public Date chuyenChuoiThanhNgay(String date) {
        try {
            return new SimpleDateFormat("dd/MM/yyyy").parse(date);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return null;
    }

    private String chuyenNgayThanhChuoi(Date date) {
        return new SimpleDateFormat("dd/MM/yyyy").format(date);
    }

    private void napDuLieuVaoBang() {
        xoaDuLieuCu(dtmGiangViens);
        int stt = 0;
        for (GiangVien gv : danhSachs.giangViens) {
            themMotDong(++stt, gv);
        }

    }

    private void xoaDuLieuCu(DefaultTableModel dtm) {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
    }

    private void themMotDong(int stt, GiangVien gv) {
        if (gv instanceof GVCoHuu) {
            dtmGiangViens.addRow(new Object[]{
                "" + stt, gv.getMa(), gv.getHoTen(), gv.getSoGio(), gv.isPhai(), chuyenNgayThanhChuoi(((GVCoHuu) gv).getNgayVao()), ((GVCoHuu) gv).getLuongCB(), null, ((GVCoHuu) gv).luong(), ((GVCoHuu) gv).phuCap()
            });
        } 
        else {
            dtmGiangViens.addRow(new Object[]{
                "" + stt, gv.getMa(), gv.getHoTen(), gv.getSoGio(), gv.isPhai(), null, null, ((GVThinhGiang) gv).getMaHopDong(), ((GVThinhGiang) gv).luong(), ((GVThinhGiang) gv).phuCap()
            }); 
        }
    }

    private void ClearAllField() {
        txtHoTen.setText("");
        txtLuongCoBan.setText("");
        txtMa.setText("");
        txtMaHopDong.setText("");
        txtNgayVao.setText("");
        txtSoGio.setText("");

    }

    private void themMotDongtb(int stt, GiangVien gv, DefaultTableModel dtm) {
        dtm.addRow(new Object[]{
            "" + stt, gv.getMa(), gv.getHoTen(), gv.getSoGio(), gv.isPhai(), chuyenNgayThanhChuoi(((GVCoHuu) gv).getNgayVao()), ((GVCoHuu) gv).getLuongCB(), ((GVCoHuu) gv).luong(), ((GVCoHuu) gv).phuCap(), (((GVCoHuu) gv).luong() + ((GVCoHuu) gv).phuCap()) / 2
        });
    }

    private void tinhTrungBinh() {
        int stt = 0;
        DefaultTableModel dtmTB = (DefaultTableModel) tblTBL.getModel();
        tblTBL.setModel(dtmTB);
        xoaDuLieuCu(dtmTB);
        for (GiangVien gv : danhSachs.giangViens) {
            if (gv instanceof GVCoHuu) {
                GVCoHuu gvch = (GVCoHuu) gv;

                themMotDongtb(++stt, gvch, dtmTB);
            }
        }
    }

