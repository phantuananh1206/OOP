    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        String ma = JOptionPane.showInputDialog("Nhap ma can tim : ");
        if ((danhSachs.tim(ma)) != null) {
            xoaDuLieuCu(dtmGiangViens);
            themMotDong(1, danhSachs.tim(ma));
            hienThiThongBao("Tim thanh cong");
        } else {
            hienThiThongBao("Khong tim thay");
        }
    }//GEN-LAST:event_btnTimActionPerformed

