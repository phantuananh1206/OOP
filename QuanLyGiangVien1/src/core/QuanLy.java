    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (jcbLoaiGiangVien.getSelectedIndex() == 0) {
            danhSachs.them(new GVCoHuu(chuyenChuoiThanhNgay(txtNgayVao.getText().trim()),
                    Double.parseDouble(txtLuongCoBan.getText().trim()),
                    txtMa.getText().trim(),
                    txtHoTen.getText().trim(),
                    Integer.parseInt(txtSoGio.getText().trim()),
                    gt));
            hienThiThongBao("Thêm thành công");
            napDuLieuVaoBang();
            ClearAllField();
        } else {
            danhSachs.them(new GVThinhGiang(txtMaHopDong.getText().trim(),
                    txtMa.getText().trim(),
                    txtHoTen.getText().trim(),
                    Integer.parseInt(txtSoGio.getText().trim()),
                    gt));
            hienThiThongBao("Thêm thành công");
            napDuLieuVaoBang();
            ClearAllField();
        }
        
    }//GEN-LAST:event_btnThemActionPerformed

