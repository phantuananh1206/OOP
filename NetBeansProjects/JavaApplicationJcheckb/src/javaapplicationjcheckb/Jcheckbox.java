    private void btselectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btselectActionPerformed
        String tmpHienThi="";
        for(Component control:jpnsothich.getComponents()){
            if(control instanceof JCheckBox){
                JCheckBox jck = (JCheckBox) control;
                if(jck.isSelected()){
                    tmpHienThi += jck.getText()+",";
                }
            }
        }
        int leng=-1;
        leng = tmpHienThi.length();
        if(leng>0){
           tmpHienThi = tmpHienThi.substring(0,leng - 1);
        }
        jTextField1.setText(tmpHienThi);
    }//GEN-LAST:event_btselectActionPerformed

