    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        // Thêm vào cơ sở dữ liệu (trong bài này là thêm vào danh sách lưu trong arraylist)
        boolean isAddOK = danhSach.themSinhVien(
            new SinhVien(
                txtMa.getText().trim(),
                txtTen.getText().trim(),
                txtLop.getText().trim(),
                txtDiaChi.getText().trim()
            )
        );
        // Nạp toàn bộ danh sách sinh viên vào bảng
        napDuLieuVaoBang();
        if (isAddOK) {
            ClearAllField();
            hienThiThongBao("THÊM MỚI THÀNH CÔNG !");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        String ma = "";
        if (selectedIndex!=-1) {
            ma = dtmSinhViens.getValueAt(selectedIndex, 1).toString();
        }
        //txtMa.getText().trim()
        // Tiến hành xóa sinh viên có mã chỉ định trong cơ sở dữ liệu (trong bài này là danh sách)
        if (danhSach.xoaSinhVien(ma)) {
            hienThiThongBao("XÓA THÀNH CÔNG !");
            napDuLieuVaoBang();
            selectedIndex = -1;
        }
    }//GEN-LAST:event_btnXoaActionPerformed

