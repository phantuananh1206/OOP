    private void myInits() {
        setTitle("Quản lý sinh viên");
        
        setLocationRelativeTo(null);
        
        dtmSinhViens = (DefaultTableModel) tblSinhViens.getModel();
        tblSinhViens.setModel(dtmSinhViens);
        
        danhSach = new DanhSachSinhVien();
    }

    private void napDuLieuVaoBang() {
        xoaDuLieuCuTrongModel();
        int stt = 0;
        for (SinhVien sinhVien : danhSach.layDanhSach()) {
            themMotDong(++stt,sinhVien);
        }
    }
    //xoa xong roi in ra phan tu vua add len table
    private void xoaDuLieuCuTrongModel() {
        while(dtmSinhViens.getRowCount()>0) {
            dtmSinhViens.removeRow(0);
        }
    }
    // them dong cho table
    private void themMotDong(int stt, SinhVien sinhVien) {
        dtmSinhViens.addRow(new Object[]{
            ""+stt,sinhVien.getMa(),sinhVien.getTen(),sinhVien.getLop(),sinhVien.getDiaChi()
        }
        );
    }
    
    private void hienThiThongBao(String thongBao){
        JOptionPane.showMessageDialog(null,
        thongBao,
        "=== THÔNG BÁO ===",
        JOptionPane.INFORMATION_MESSAGE);
    }

    private void ClearAllField() {
        txtMa.setText("");
        txtTen.setText("");
        txtLop.setText("");
        txtDiaChi.setText("");
        
        txtMa.requestFocus();
    }
